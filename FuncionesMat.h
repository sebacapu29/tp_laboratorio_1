#ifndef FUNCIONESMAT_H_INCLUDED
#define FUNCIONESMAT_H_INCLUDED

/** \brief Funcion que suma 2 valores
 *
 * \param valorDos flotante a sumar
 * \param valorUno flotante a sumar
 * \return valor flotante
 *
 */

float Suma(float valorUno, float valorDos);

/** \brief Funcion que resta 2 valores
 *
 * \param valorUno primer valor a restar
 * \param valorDos segundo valor a restar.
 * \return Devuelve un valor Flotante.
 *
 */

float Resta(float valorUno, float valorDos);

/** \brief Funcion que multiplica 2 valores.
 *
 * \param valorUno primer valor a multiplicar.
 * \param valorDos segundo valor a multiplicar.
 * \return Devuelve un flotante.
 *
 */

float Multiplicacion(float valorUno, float valorDos);

/** \brief Divide 2 valores flotantes.
 *
 * \param valorUno es el dividendo.
 * \param valorDos es el divisor.
 * \return Devuelve un flotante.
 *
 */

float Division(float valorUno, float valorDos);

#endif // LIBRERIAFUNCIONESMAT_H_INCLUDED
